#include <stdio.h>
#include <stdlib.h>

/**
 * main - Prints the name of the program
 * @argc: The number of command-line arguments
 * @argv: An array of command-line argument strings
 *
 * Return: Always 0
 */

int main (int argc, char* argv[])
{
printf("%s\n", argv[0]);
(void)argc;

return (0);
}
